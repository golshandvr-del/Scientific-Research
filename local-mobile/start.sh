#!/data/data/com.termux/files/usr/bin/bash
# =============================================================================
#  start.sh — راه‌اندازیِ سرورِ کاملِ سایت روی گوشی (Termux)
# =============================================================================
#  این اسکریپت:
#    ۱) در صورتِ نبودِ Node، آن را نصب می‌کند.
#    ۲) wake-lock می‌گیرد تا با خاموش‌شدنِ صفحه، سرور قطع نشود.
#    ۳) سرورِ سایت (موتورِ کامل) را روی 0.0.0.0:8080 اجرا می‌کند تا هم روی
#       همین گوشی و هم برای دوستتان در شبکهٔ محلی (LAN) در دسترس باشد.
#
#  اجرا:  bash start.sh
#  توقف:  کلیدهای Ctrl+C  (یا: bash stop.sh)
# =============================================================================
set -e

PORT="${PORT:-8080}"
HOST="${HOST:-0.0.0.0}"
DIR="$(cd "$(dirname "$0")" && pwd)"

echo "=============================================================="
echo "  🧭 راه‌اندازیِ دستیارِ تصمیمِ معاملاتی (موتورِ کاملِ سایت)"
echo "=============================================================="

# --- ۱) اطمینان از نصبِ Node --------------------------------------------------
if ! command -v node >/dev/null 2>&1; then
  echo "▶ Node یافت نشد؛ در حالِ نصب (نیاز به اینترنت) ..."
  pkg update -y >/dev/null 2>&1 || true
  pkg install -y nodejs >/dev/null 2>&1
fi
echo "  Node: $(node --version)"

# --- ۲) wake-lock تا سرور با خاموشیِ صفحه قطع نشود ---------------------------
if command -v termux-wake-lock >/dev/null 2>&1; then
  termux-wake-lock || true
  echo "  🔒 wake-lock فعال شد (سرور با خاموش‌شدنِ صفحه ادامه می‌دهد)."
else
  echo "  ⚠️  termux-wake-lock نصب نیست؛ برای پایداریِ بهتر در پس‌زمینه بزنید:"
  echo "        pkg install termux-api"
fi

# --- ۳) بررسیِ وجودِ باندلِ آماده --------------------------------------------
if [ ! -f "$DIR/app.bundle.mjs" ]; then
  echo "  ❌ app.bundle.mjs یافت نشد. مطمئن شوید git pull کامل انجام شده است."
  exit 1
fi

# آزادسازیِ پورت اگر از قبل اشغال بود
if command -v fuser >/dev/null 2>&1; then
  fuser -k "${PORT}/tcp" >/dev/null 2>&1 || true
fi

echo "  ▶ اجرای سرور روی http://${HOST}:${PORT} ..."
echo "--------------------------------------------------------------"
cd "$DIR"
exec env PORT="$PORT" HOST="$HOST" node server.mjs
