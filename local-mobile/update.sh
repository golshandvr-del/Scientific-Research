#!/data/data/com.termux/files/usr/bin/bash
# =============================================================================
#  update.sh — به‌روزرسانیِ دستیِ پروژه روی گوشی (بدونِ کلونِ دوباره)
# =============================================================================
#  این اسکریپت فقط تغییراتِ جدید را از گیت‌هاب می‌گیرد (git pull) و چون باندلِ
#  آمادهٔ سایت (app.bundle.mjs) هم در گیت است، نسخهٔ جدیدِ منطقِ سایت به‌صورتِ
#  خودکار می‌آید — نیازی به build روی گوشی نیست.
#
#  کاربرد: هر وقت خواستید پروژه را به آخرین نسخه برسانید:
#      bash update.sh
#  سپس سرور را دوباره اجرا کنید:
#      bash start.sh
#
#  توجه: این آپدیت «دستی» است؛ هیچ‌چیز خودکار به‌روزرسانی نمی‌شود.
# =============================================================================
set -e

DIR="$(cd "$(dirname "$0")" && pwd)"
REPO="$(cd "$DIR/.." && pwd)"   # ریشهٔ کلونِ گیت (Scientific-Research)

echo "=============================================================="
echo "  ⬇️  به‌روزرسانیِ دستیِ پروژه از گیت‌هاب"
echo "=============================================================="
cd "$REPO"

# اگر تغییرِ محلیِ ناخواسته‌ای بود، آن را کنار می‌گذاریم تا pull تمیز انجام شود.
if ! git diff --quiet || ! git diff --cached --quiet; then
  echo "  ⚠️  تغییرِ محلی دیده شد؛ برای pullِ تمیز کنار گذاشته می‌شود (stash)."
  git stash push -u -m "auto-stash-before-update-$(date +%s)" || true
fi

echo "  ▶ git pull ..."
git pull --ff-only origin main

echo "--------------------------------------------------------------"
echo "  ✅ به‌روزرسانی کامل شد. اکنون سرور را اجرا/ری‌استارت کنید:"
echo "        bash \"$DIR/start.sh\""
echo "=============================================================="
