# -*- coding: utf-8 -*-
"""
market_spec.py — مشخصاتِ واقعیِ بروکر/بازار (کالیبره‌شده با حسابِ دموِ واقعیِ کاربر)
================================================================================

این فایل تنها منبعِ حقیقت (single source of truth) برای هزینه‌ها، اندازهٔ قرارداد،
مارجین و اسپردِ همهٔ نمادها است. کالیبراسیون از دادهٔ واقعیِ حسابِ دموِ کاربر
(نشستِ ۲۰۲۶-۰۷) استخراج شده است.

--------------------------------------------------------------------------------
مدرکِ کالیبراسیون (نمونهٔ واقعیِ کاربر روی XAUUSD):
    Long 0.01 lot | Entry: 4037.68 | TP: 4050.50 | Profit: +12.82$
    => حرکتِ قیمت = 12.82$  ⇒  سود = 12.82$ برای 0.01 لات
    => CONTRACT_SIZE = 100  (هر ۱ لات = ۱۰۰ اونس ⇒ حرکتِ ۱.۰۰ قیمت = ۱۰۰$/لات)  ✅

    اسپردِ لحظه‌ای: bid=4069.27  ask=4069.60  ⇒  اسپرد = 0.33$ = ۳۳ پوینت ≈ ۳.۳ پیپ
    مارجین: 0.4$ برای 0.01 لات  ⇒  40$ برای ۱ لات (لوریجِ مؤثرِ بالا)
    کمیسیونِ جدا: کاربر هیچ کمیسیونِ جداگانه‌ای اعلام نکرد ⇒ هزینه فقط از اسپرد است.

--------------------------------------------------------------------------------
🎯 قانونِ کالیبراسیونِ هزینهٔ واقعی (باید در همهٔ اسناد تکرار شود):
    مدلِ قدیمیِ پروژه هزینه را ۵۷$/لات فرض می‌کرد (اسپرد ۵۰$ + کمیسیون ۷$)، در حالی
    که واقعیتِ حسابِ کاربر فقط ۳۳$/لات (اسپردِ خالص، بدون کمیسیونِ جدا) است — یعنی
    مدلِ قدیمی ۱.۷× بدبینانه‌تر بود. بنابراین:
      • هر استراتژیِ سوددهی که با مدلِ قدیمی سبز شده، در واقعیت سودده‌تر است (معتبر ✅).
      • استراتژی‌هایی که نزدیکِ break-even رد شده‌اند، باید با هزینهٔ واقعی بازآزمایی شوند.
    از این پس هزینهٔ استاندارد = «اسپردِ رفت‌وبرگشتِ واقعی + slippageِ کوچک»، بدونِ
    دوباره‌شماریِ کمیسیون.
--------------------------------------------------------------------------------
"""

# ============================ XAUUSD (طلا) ============================
# اندازهٔ قرارداد: ۱ لات = ۱۰۰ اونس  ⇒ حرکتِ ۱.۰۰ دلارِ قیمت = ۱۰۰$ برای ۱ لات
XAU_CONTRACT_SIZE = 100.0
XAU_PIP = 0.10                      # ۱ پیپِ طلا = ۰.۱۰ دلارِ قیمت (۱۰ پوینت)

# اسپردِ واقعیِ کاربر = 0.33$ (≈۳.۳ پیپ). برای محافظه‌کاریِ اندک ۰.۳۵$ می‌گیریم.
XAU_SPREAD_PRICE = 0.35             # اسپردِ رفت‌وبرگشت بر حسبِ قیمت ($/اونس) — یکبار پرداخت
XAU_SLIPPAGE_PRICE = 0.05           # لغزشِ کوچکِ اجرا ($/اونس، رفت‌وبرگشت)
# هزینهٔ کلِ رفت‌وبرگشت بر حسبِ قیمت (روی ۱ اونس) — این از حرکتِ قیمت کم می‌شود:
XAU_COST_PRICE = XAU_SPREAD_PRICE + XAU_SLIPPAGE_PRICE   # = 0.40$/اونس = 40$/لات

# کمیسیونِ جدا: صفر (کاربر کمیسیونِ جداگانه ندارد؛ هزینه فقط از اسپرد است)
XAU_COMMISSION_PER_LOT = 0.0
XAU_MARGIN_PER_LOT = 40.0           # مارجینِ لازم برای ۱ لات ($)
XAU_MIN_LOT = 0.01

# ============================ EURUSD (فارکس) ============================
# اندازهٔ استانداردِ فارکس: ۱ لات = ۱۰۰٬۰۰۰ واحد  ⇒ ۱ پیپ (0.0001) = 10$/لات
EUR_CONTRACT_SIZE = 100_000.0
EUR_PIP = 0.0001
EUR_SPREAD_PRICE = 0.00012          # ≈۱.۲ پیپ اسپردِ معمولِ EURUSD ($/واحدِ قیمت)
EUR_SLIPPAGE_PRICE = 0.00003
EUR_COST_PRICE = EUR_SPREAD_PRICE + EUR_SLIPPAGE_PRICE
EUR_COMMISSION_PER_LOT = 0.0
EUR_MARGIN_PER_LOT = 1000.0
EUR_MIN_LOT = 0.01


SPECS = {
    'XAUUSD': {
        'contract_size': XAU_CONTRACT_SIZE,
        'pip': XAU_PIP,
        'spread_price': XAU_SPREAD_PRICE,
        'slippage_price': XAU_SLIPPAGE_PRICE,
        'cost_price': XAU_COST_PRICE,
        'commission_per_lot': XAU_COMMISSION_PER_LOT,
        'margin_per_lot': XAU_MARGIN_PER_LOT,
        'min_lot': XAU_MIN_LOT,
    },
    'EURUSD': {
        'contract_size': EUR_CONTRACT_SIZE,
        'pip': EUR_PIP,
        'spread_price': EUR_SPREAD_PRICE,
        'slippage_price': EUR_SLIPPAGE_PRICE,
        'cost_price': EUR_COST_PRICE,
        'commission_per_lot': EUR_COMMISSION_PER_LOT,
        'margin_per_lot': EUR_MARGIN_PER_LOT,
        'min_lot': EUR_MIN_LOT,
    },
}


def get_spec(symbol):
    """مشخصاتِ کاملِ یک نماد را برمی‌گرداند (پیش‌فرض: XAUUSD)."""
    return SPECS.get(symbol.upper(), SPECS['XAUUSD'])


if __name__ == '__main__':
    for sym, s in SPECS.items():
        print(f"=== {sym} ===")
        print(f"  contract_size = {s['contract_size']:,.0f}")
        print(f"  cost_price    = {s['cost_price']:.5f}  "
              f"(= {s['cost_price']*s['contract_size']:.2f}$/لات)")
        print(f"  commission    = {s['commission_per_lot']:.2f}$/لات")
        print(f"  margin        = {s['margin_per_lot']:.0f}$/لات")
